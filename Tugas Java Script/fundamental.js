// Menggunakan berbagai tipe data

// String
let name = "Agita Irvanda";
console.log("Name:", name);
alert("Nama saya adalah: " + name);

// Number
let age = 21;
console.log("Age:", age);
alert("Umur saya adalah: " + age);

// Boolean
let isStudent = true;
console.log("Is student:", isStudent);
alert("Apakah saya seorang Mahasiswa? " + isStudent);

// Array
let Food = ["GoodDay", "Tehtarik", "Jasjus"];
console.log("Food:", Food);
alert("Saya suka minum ini: " + Food.join(", "));

// Object
let person = {
  firstName: "agita",
  lastName: "irvanda saputra",
  age: 21
};
console.log("Person:", person);
alert("Nama lengkap: " + person.firstName + " " + person.lastName + ", Umur: " + person.age);

// Null
let emptyValue = null;
console.log("Null value:", emptyValue);

// Undefined
let undefinedValue;
console.log("Undefined value:", undefinedValue);